export function projectLonLat(lon, lat) {
  return [(lon + 180) / 360 * 720, (90 - lat) / 180 * 360 + 20];
}

// Draws the land outlines into the current (already scaled) 720x400 design space.
export function drawWorldMap(ctx) {
  ctx.fillStyle = "#141A26";
  ctx.strokeStyle = "#2B3344";
  ctx.lineWidth = 1;
  for (const poly of LANDMASSES) {
    ctx.beginPath();
    poly.forEach(([lon, lat], i) => {
      const [x, y] = projectLonLat(lon, lat);
      if (i) ctx.lineTo(x, y); else ctx.moveTo(x, y);
    });
    ctx.closePath();
    ctx.fill();
    ctx.stroke();
  }
}
